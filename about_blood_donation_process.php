                <div class="sub_container">
                    <h2>Dont's Before Blood Donation?</h2>
                    <ul type="bullet">
                        <li><strong> Never Skip Meals :</strong> Donating blood on an empty stomach is discouraged. Proper
                            nutrition provides
                            the
                            energy needed for the donation and helps prevent fatigue and dizziness.</li>
                        <li><strong>Avoid Alcohol :</strong> Refrain from consuming alcohol at least 24 hours before
                            donating. Alcohol can
                            dehydrate your body, affecting the quality of your blood.</li>
                        <li><strong>Say No to Smoking :</strong> Smoking immediately before donating can impact your blood's
                            oxygen-carrying
                            capacity. It's best to avoid smoking on the day of your donation.</li>
                        <li> <strong>Prioritize Health :</strong> If you're unwell or recovering from an illness, postpone
                            your donation until
                            you're fully recovered. This ensures both your well-being and the safety of the recipient.</li>
                    </ul>
                </div><br>
                <div class="sub_container">
