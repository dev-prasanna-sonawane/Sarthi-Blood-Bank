                    <ul>
                        <li><strong>Hydration is Key :</strong> Staying well-hydrated before your donation is crucial. Proper
                            hydration
                            ensures a smooth blood flow and helps you feel comfortable during the process.</li>
                        <li><strong>Nourish Your Body :</strong> Consume iron-rich foods in the days leading up to your
                            donation. Leafy
                            greens, lean meats, and legumes are excellent choices that enhance the quality of your donated
                            blood.</li>
                        <li><strong>Prioritize Rest :</strong> A good night's sleep prior to your donation is essential.
                            Being well-rested
                            contributes to a successful and positive donation experience.</li>
                        <li><strong>Honesty is Essential :</strong> Inform the medical staff about any medications you’re
                            taking. Transparency
                            about your health helps maintain the safety of both the donor and the recipient.You will
