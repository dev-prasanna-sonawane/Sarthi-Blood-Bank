                    <h2>Types of Donations</h2>
                    Blood donations are divided into groups based on who will receive the collected blood.
                    <ul>
                        <li> An <strong>'allogeneic' (also called 'homologous') donation</strong> is when a donor gives blood for <strong>storage at a blood bank</strong> for transfusion to an <strong>unknown recipient</strong>.</li>
                        <li> A <strong>'directed' donation</strong> is when a person, often a family member, donates blood for
                            <strong>transfusion to a specific individual</strong>. Directed donations are relatively rare when an established supply exists.
                        </li>
                        <li> A <strong>'replacement donor'</strong> donation is a hybrid of the two and is common in developing countries. In this case, a friend or family member of the recipient <strong>donates blood to replace the stored blood used in a transfusion</strong>, ensuring a consistent supply. When a person has blood stored that will be transfused back to the donor at a later date, usually after surgery, that is called an <strong>'autologous donation'</strong>.</li>
                    </ul>
                    Blood that is used to make medications can be made from allogeneic donations or from donations exclusively
                    used for manufacturing.<br>
                    An event where donors come to <strong>donate allogeneic blood</strong> is sometimes called a <strong>'Blood Donation Campaign' </strong>. These can occur at a blood bank, but they are often set up at a location in the community such as a shopping center, workplace, school, or house of worship.
                </div><br>
