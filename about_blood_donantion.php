                    <h2>Whole blood</h2>
                    The most common method is collecting the blood from the donor's vein into a container. <strong>The amount of blood drawn </strong>varies from 200 millilitres to 550 millilitres depending on the country, but <strong>400 millilitres is typical in India</strong>.<br> The blood is usually <strong>stored in a flexible plastic bag that also contains sodium citrate, phosphate, dextrose, and adenine. This combination keeps the blood from clotting and preserves it during storage up to 42 days</strong>. Other chemicals are sometimes added during processing.
