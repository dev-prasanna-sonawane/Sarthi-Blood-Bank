                    <h2>Demand for blood</h2>
                    India requires approximately 1.45 crore units of blood annually. This blood is collected through 3,500 licensed blood banks across the country.<br>
                    One type of
                    blood being in stock does not guarantee that another type is. Blood banks may have some units in stock but
                    lack others, ultimately causing the patients that need units for specific blood types to have delayed or
                    canceled procedures.<br> Additionally, every year there is an increase of around 5-7% for transfusions
                    without an increase of donors to balance it as well as a growing population of elderly people that will need
                    more transfusions in the future without a predicted increase in donations to reflect those growing
                    numbers.<br>
                    Blood donations tend to always be high in demand with numerous accounts repeatedly stating periodic
                    shortages over the decades. The limited storage time means that it is difficult to have a stockpile of blood to prepare for a disaster.
