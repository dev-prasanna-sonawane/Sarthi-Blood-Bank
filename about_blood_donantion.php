                    <h2> Storage and blood shelf life</h2>
                    The collected blood is usually stored in a blood bank as separate components, and some of these have short
                    shelf lives. <ul>
                        <li>The longest shelf life used for <strong>platelets</strong> is <strong>seven days</strong>.</li>
                        <li><strong>Red blood cells (RBC)</strong>,
                            the most frequently used component, have a shelf life of <strong>35–42 days</strong> at refrigerated temperatures. For
                            (relatively rare) long-term storage applications, this can be extended by freezing the blood with a mixture
                            of glycerol, but this process is expensive and requires an extremely cold freezer for storage.</li>
                        <li><strong> Plasma</strong> can be
                            stored frozen for an extended period of time and is typically given an expiration date of <strong>one year</strong> and maintaining a supply is less of a problem.</li>
                    </ul>
