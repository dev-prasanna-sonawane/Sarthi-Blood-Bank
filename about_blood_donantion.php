                        <h2>Blood Tests</h2>
                        The donor's blood type must be determined if the blood will be used for transfusions.<br> The collecting agency usually identifies whether the blood is type A, B, AB, or O and the donor's Rh (D) type and will screen for antibodies to less common antigens. More testing, including a crossmatch, is usually done before a transfusion.<br><strong>Type O negative is often cited as the "universal donor" but this only refers to red cell and whole blood transfusions. For plasma and platelet transfusions the system is reversed: AB positive is the universal platelet donor type while both AB positive and AB negative are universal plasma donor types.</strong><br>
                        Most blood is tested for diseases, including some STDs. The tests used are high-sensitivity
                        screening tests and no actual diagnosis is made. Some of the test results are later found to be false
                        positives using more specific testing. False negatives are rare, but donors are discouraged from
                        using blood donation for the purpose of anonymous STD screening because a false negative could mean a
                        contaminated unit. The blood is usually discarded if these tests are positive, but there are some
                        exceptions, such as autologous donations. The donor is generally notified of the test result.<br>
                        Donated blood is tested by many methods, but the <strong>core tests</strong> recommended by the World Health Organization are these four:<strong> Hepatitis B surface antigen Antibody to hepatitis C Antibody to HIV, usually subtypes 1 and 2 Serologic test for syphilis</strong><br>
                        The WHO reported in 2006 that 56 out of 124 countries surveyed did not use these basic tests on all
                        blood donations.

                        A variety of other tests for transfusion transmitted infections are often used based on local
                        requirements. Additional testing is expensive, and in some cases the tests are not implemented because
                        of the cost. These additional tests include other infectious diseases such as West Nile fever
                        and babesiosis. Sometimes multiple tests are used for a single disease to cover the limitations of
                        each test. For example, the HIV antibody test will not detect a recently infected donor, so some blood
                        banks use a p24 antigen or HIV nucleic acid test in addition to the basic antibody test to detect
                        infected donors. Cytomegalovirus is a special case in donor testing in that many donors will test
                        positive for it. The virus is not a hazard to a healthy recipient, but it can harm infants and
                        other recipients with weak immune systems.
