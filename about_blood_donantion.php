                    <h2>Recovery and time between donations</h2>
                    Donors are usually kept at the donation site for 10–15 minutes after donating since most adverse reactions
                    take place during or immediately after the donation. Blood centers typically provide light refreshments,
                    such as orange juice and cookies, or a lunch allowance to help the donor recover.<br> The needle site is
                    covered with a bandage and the donor is directed to keep the bandage on for several hours. In hot
                    climates, donors are advised to avoid dehydration (strenuous exercise and games, alcohol) until a few hours
                    after donation.

                    Donated plasma is replaced after 2–3 days.<br> Red blood cells are replaced by bone marrow into the
                    circulatory system at a slower rate, on average 36 days in healthy adult males.<br>

                    Plasmapheresis and plateletpheresis donors can donate much more frequently because they do not lose
                    significant amounts of red cells.<br> The exact rate of how often a donor can donate differs from country to
                    country. <br>For example, plasmapheresis donors in the United States are allowed to donate large volumes twice a
                    week and could nominally donate 83 litres (about 22 gallons) in a year, whereas the same donor in Japan may
                    only donate every other week and could only donate about 16 litres (about 4 gallons) in a year.
                    <br>Whole Blood Donation can be done after 3 months of previous Donation<br>

                    Iron supplementation decreases the rates of donor deferral due to low hemoglobin, both at the first donation
                    visit and at subsequent donations. Iron-supplemented donors have higher hemoglobin and iron stores. On the
                    other hand, iron supplementation frequently causes diarrhea, constipation and epigastric abdominal
                    discomfort. The long-term effects of iron supplementation without measurement of iron stores are unknown.
